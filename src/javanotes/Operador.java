package javanotes;

public class Operador {
    public static void main(String[] args) {
        //hay tipo 5 de operadores: aritmeticos:(+,-,*,/,%)
        int num1 = 50;

        int num2 = 35;

        int sum= num1 + num2;

        System.out.println(sum);

        int rest = num1 - num2;

        System.out.println(rest);

        int mult = num1 * num2;

        System.out.println(mult);

        int div = num1 / num2;

        System.out.println(div);
    }
}
